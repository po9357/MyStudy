-- SWQUENCE 생성
CREATE SEQUENCE SEQUENCE1;

-- 사용 : NEXVAL(다음번호 채번), CURRVAL(현재 번호)
SELECT SEQUENCE1.NEXTVAL FROM DUAL;

SELECT SEQUENCE1.CURRVAL FROM DUAL;
